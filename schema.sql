-- Indexes for fast searching
CREATE INDEX idx_leads_zip ON leads (zip_code);
CREATE INDEX idx_leads_last_name ON leads (last_name);
CREATE INDEX idx_leads_occupation_code ON leads (occupation_code);
CREATE INDEX idx_leads_doc_type_code ON leads (doc_type_code);

-- Foreign Keys (allow NULLs)
ALTER TABLE leads ADD CONSTRAINT fk_leads_occupation_code
  FOREIGN KEY (occupation_code) REFERENCES dict_occupations(code) ON DELETE SET NULL;

ALTER TABLE leads ADD CONSTRAINT fk_leads_doc_type_code
  FOREIGN KEY (doc_type_code) REFERENCES dict_doc_types(code) ON DELETE SET NULL;

ALTER TABLE leads ADD CONSTRAINT fk_leads_prop_cl_ind
  FOREIGN KEY (prop_cl_ind) REFERENCES dict_property_class(code) ON DELETE SET NULL;

COMMENT ON TABLE leads IS 'Ziarem CRM main lead table (~1M rows). FA + CP Data Appended columns + ziarem_tags (Wolf/Dispute/Lyco).';
