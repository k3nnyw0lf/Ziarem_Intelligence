  code        VARCHAR(50) PRIMARY KEY,
  description TEXT NOT NULL
);

-- Map from DOC_TYPE.csv
CREATE TABLE dict_doc_types (
  code        VARCHAR(50) PRIMARY KEY,
  description TEXT NOT NULL
);

-- Map from PROP_CL_IND.csv
CREATE TABLE dict_property_class (
  code        VARCHAR(50) PRIMARY KEY,
  description TEXT NOT NULL
);

